public class Feedback {
	
	//properties
	String userFeedback;
	int userRating;
	
	//constructors
	
	//methods
	//setters
	public void setFeedback(String f) {
		userFeedback = f;
	}
	
	public void setRating(int r) {
		userRating = r;
	}
	
	//getters
	public String getFeedback() {
		return userFeedback;
	}
}
